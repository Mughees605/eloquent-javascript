// A primitive (primitive value, primitive data type) is data that is not an object and 
// has no methods.
//  In JavaScript, there are 6 primitive data types:
//  string, number, boolean, null, undefined, symbol (new in ECMAScript 2015).

// Most of the time, a primitive value is represented directly at the lowest level of the language implementation.

// All primitives are immutable (cannot be changed).